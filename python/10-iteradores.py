#Los iteradores corren un determinado numero de veces una pieza de codigo.__spec__

meses= ['octubre', 'febrero', 'marzo']

# CICLO FOR
for mes in meses:
    print(mes) 

print(f'estoy viajando por el mundo en el mes de {mes}')

#imprimir numeros
for numero in range(0,20,5):
    print(numero)

